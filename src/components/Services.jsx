// Mapeo automático de títulos a iconos
const titleToIcon = {
  "Desarrollo Web": "code",
  "Inteligencia Artificial": "ai",
  "Full Stack": "monitor",
  "Aplicaciones": "mobile",
  "Backend": "server",
  "Frontend": "layout",
  "Automatización": "settings",
  "Base de Datos": "database",
  "Cloud": "cloud",
  "Seguridad": "shield",
};

// Función para obtener el icono correcto
const getIcon = (service) => {
  // Si el servicio tiene un icono específico, usarlo
  if (service.icon && ServiceIcons[service.icon]) {
    return ServiceIcons[service.icon];
  }
  // Si no, intentar mapear por título
  const iconKey = titleToIcon[service.title];
  if (iconKey && ServiceIcons[iconKey]) {
    return ServiceIcons[iconKey];
  }
  // Default
  return ServiceIcons["layers"];
};

export default function Services({ id = "servicios" }) {
  const { content } = useContent();
  const { services } = content;
  const [isVisible, setIsVisible] = useState(false);
  const sectionRef = useRef(null);
  const gridRef = useRef(null);

  // Intersection Observer para detectar cuando la sección es visible
  useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true);
        } else {
          setIsVisible(false);
        }
      },
      { threshold: 0.2 }
    );

    if (sectionRef.current) {
      observer.observe(sectionRef.current);
    }

    return () => observer.disconnect();
  }, []);

  // Scroll horizontal con rueda del mouse
  useEffect(() => {
    const grid = gridRef.current;
    if (!grid) return;

    const handleWheel = (e) => {
      if (e.deltaY !== 0) {
        e.preventDefault();
        grid.scrollLeft += e.deltaY;
      }
    };

    grid.addEventListener('wheel', handleWheel, { passive: false });
    return () => grid.removeEventListener('wheel', handleWheel);
  }, []);

  // Funciones de navegación manual
  const scrollToLeft = () => {
    if (gridRef.current) {
      gridRef.current.scrollBy({ left: -350, behavior: 'smooth' });
    }
  };

  const scrollToRight = () => {
    if (gridRef.current) {
      gridRef.current.scrollBy({ left: 350, behavior: 'smooth' });
    }
  };

  return (
    <section id={id} className="section" ref={sectionRef}>
      <div className="container">
        <div className={`section__header ${isVisible ? 'animate-fade-up' : ''}`}>
          <h2>Servicios</h2>
          <p>Esto es lo que puedo hacer por tu proyecto.</p>
        </div>

        <div className="services__wrapper">
          {/* Flecha izquierda */}
          <button 
            className={`services__nav services__nav--prev ${isVisible ? 'animate-in' : ''}`} 
            onClick={scrollToLeft}
          >
            <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
              <polyline points="15 18 9 12 15 6" />
            </svg>
          </button>

          <div className="services__grid" ref={gridRef}>
            {services.map((srv, i) => (
              <div 
                key={i} 
                className={`service ${isVisible ? 'animate-in' : ''}`}
                style={{ animationDelay: `${i * 0.15 + 0.2}s` }}
              >
                <div className="service__icon">
                  {getIcon(srv)}
                </div>
                <h3 className="service__title">{srv.title}</h3>
                <p className="service__desc">{srv.desc}</p>
              </div>
            ))}
          </div>

          {/* Flecha derecha */}
          <button 
            className={`services__nav services__nav--next ${isVisible ? 'animate-in' : ''}`} 
            onClick={scrollToRight}
          >
            <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
              <polyline points="9 18 15 12 9 6" />
            </svg>
          </button>
        </div>

        <div className={`center ${isVisible ? 'animate-fade-up' : ''}`} style={{ animationDelay: '0.8s' }}>
          <a 
            href="https://wa.me/542614856964?text=Hola%20Bruno%2C%20quer%C3%ADa%20hablarte%20para%20cotizar%20un%20proyecto." 
            className="btn btn--outline-arrow"
            target="_blank"
            rel="noreferrer"
          >
            Contáctame
            <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" width="18" height="18">
              <line x1="5" y1="12" x2="19" y2="12" />
              <polyline points="12 5 19 12 12 19" />
            </svg>
          </a>
        </div>
      </div>
    </section>
  );
}